/* ESERCIZIO 1
  Scrivi una funzione per concatenare due stringhe ricevute come parametri, selezionando solamente i primi 2 caratteri della
  prima e gli ultimi 3 della seconda. Converti la stringa risultante in maiuscolo e mostrala con un console.log().
*/
console.log("ESERCIZIO 01");

const concatStrings = function (firstString, secondString) {
  const splitFirstString = firstString.split("");
  const splitSecondString = secondString.split("");
  const newArray1 = splitFirstString.slice(0, 2);
  const newArray2 = splitSecondString.slice(splitSecondString.length - 3);
  return newArray1.join("").toUpperCase() + newArray2.join("").toUpperCase();
};

console.log(concatStrings("Prima stringa di prova", "Seconda stringa di test"));

/* ESERCIZIO 2 (for)
  Scrivi una funzione che torni un array di 10 elementi; ognuno di essi deve essere un valore random compreso tra 0 e 100 (incluso).
*/
console.log("ESERCIZIO 02");

const getRandomNum = function (num) {
  const randomArray = [];
  for (let i = 0; i < num; i++) {
    randomArray.push(Math.floor(Math.random() * 100));
  }
  return randomArray;
};
console.log(getRandomNum(10));

